    private void cargarAlumnos() {
        for(Alumno item:listaAlumnos) {
            this.cbxAlumno.addItem(item);
        }
        rbInscriptas.setSelected(true);
    }

    private void armarCabezeraTabla() {
        ArrayList<Object> columnas = new ArrayList<Object>();
        columnas.add("ID");
        columnas.add("MATERIA");
        for(Object it:columnas) {
            modelo.addColumn(it);
        }
        this.tblMaterias.setModel(modelo);
    }
    
    private void borrarFilasTabla() {
        int f = this.modelo.getRowCount() - 1;
        for (int i=f; i>=0; i--) {
            modelo.removeRow(i);
        }
    }

    private void cargarDatosTablaInscriptas() {
        borrarFilasTabla();
        
        Alumno al = (Alumno)this.cbxAlumno.getSelectedItem();
        listaMaterias = (ArrayList)cursadaData.obtenerMateriasCursadas(al.getId());
        for(Materia m:listaMaterias) {
            modelo.addRow(new Object[]{m.getId(), m.getNombre()});
        }
    }

    private void cargarDatosTablaNoInscriptas() {
        borrarFilasTabla();
        
        Alumno al = (Alumno)this.cbxAlumno.getSelectedItem();
        listaMaterias = (ArrayList)cursadaData.obtenerMateriasNOCursadas(al.getId());
        for(Materia m:listaMaterias) {
            modelo.addRow(new Object[]{m.getId(), m.getNombre()});
        }
    }
