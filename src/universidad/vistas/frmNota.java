    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int nota = Integer.parseInt(txtNota.getText());
        int fila = (Integer)tblTabla.getSelectedRow();
        if((nota>=0&&nota<=10)&&(fila != -1)){
            Materia m = (Materia)cbxMateria.getSelectedItem();
            int idAlumno = (Integer)modelo.getValueAt(fila, 0);
            
            CursadaData cd = new CursadaData(con);
            cd.actualizarNotaCursada(idAlumno, m.getId(), nota);
            JOptionPane.showMessageDialog(null, "Nota guardada con Ã©xito");
            
        }else{JOptionPane.showMessageDialog(null, "Error al modificar nota");}
        limpiar();
        listaCursada = (ArrayList)cursadaData.obtenerCursadas();
        cargarDatosTabla();
    }//GEN-LAST:event_btnGuardarActionPerformed


