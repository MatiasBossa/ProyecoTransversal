  public void cargarMaterias() {
        for (Materia item:listaMaterias) {
            this.cbxMateria.addItem(item);
        }
    }
    
    public void armarCabezeraTabla() {
        
        ArrayList<Object> columnas = new ArrayList<Object>();
        columnas.add("ID");
        columnas.add("NOMBRE");
        columnas.add("NOTA");
        for(Object it:columnas) {
            modelo.addColumn(it);
        }
        this.tblTabla.setModel(modelo);
    }
    
    public void borrarFilasTabla() {

        int f = modelo.getRowCount()-1;
        for(int i=f; i>=0; i--) {
            modelo.removeRow(i);
        }
    }
    
    public void cargarDatosTabla(){
        borrarFilasTabla();
        Materia mat = (Materia)this.cbxMateria.getSelectedItem();
        
        for (Cursada m:listaCursada){
            if (m.getMateria().getId() == mat.getId()) {
                modelo.addRow(new Object[]{m.getAlumno().getId(), m.getAlumno().getNombre(), m.getNota()});
            }
        }
    }
    
    public void limpiar(){
        txtNota.setText("");
    }
    


